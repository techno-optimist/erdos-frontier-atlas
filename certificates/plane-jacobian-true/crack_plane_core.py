# Axis shape: free p(y) + free E_x q(x) only
# ---------------------------------------------------------------------------

def prove_axis_elem_only(dmax: int = 6) -> bool:
    """In axis shape f=x+p(y)+mixed, g=y+q_x(x)+mixed', mixed must vanish
    for single mixed; full free p and pure-x in g work.
    """
    print("=== AXIS  elementary subvariety + single mixed block ===", flush=True)
    ok = True
    for D in range(2, dmax + 1):
        # free p: f=x+sum p_k y^k, g=y
        f = X
        for k in range(2, D + 1):
            f = padd(f, pscale(ppow(Y, k), Q(1)))
        ok &= check(f"axis E_y full D={D}", is_const_nz(jac_det(f, Y)))
        # free E_x
        g = Y
        for k in range(2, D + 1):
            g = padd(g, pscale(ppow(X, k), Q(1)))
        ok &= check(f"axis E_x full D={D}", is_const_nz(jac_det(X, g)))

        # each mixed monom alone
        n_m = n_b = 0
        for tot in range(2, D + 1):
            for i in range(1, tot + 1):
                j = tot - i
                mon = pmul(ppow(X, i), ppow(Y, j))
                # in f
                n_m += 1
                if not is_const_nz(jac_det(padd(X, mon), Y)):
                    n_b += 1
                # in g with j>=1 (j=0 is E_x)
                if j >= 1:
                    n_m += 1
                    if not is_const_nz(jac_det(X, padd(Y, mon))):
                        n_b += 1
        ok &= check(f"axis D={D} single mixed block", n_m == n_b, f"{n_b}/{n_m}")
    return ok


