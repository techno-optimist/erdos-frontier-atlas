def prove_T5(nmax: int = 15, dy: int = 6) -> bool:
    print("=== T5  deg_x(f)>=2 pure-x-power blocked ===", flush=True)
    ok = True
    x, y = symbols("x y")
    # Degree-free symbolic identity (P,Q free polys via symbols of bounded y-deg)
    for N in range(2, nmax + 1):
        P_c = symbols(f"P0:{dy+1}")
        Q_c = symbols(f"Q0:{dy+1}")
        P = sum(c * y**k for k, c in enumerate(P_c))
        Q = sum(c * y**k for k, c in enumerate(Q_c))
        # g=y only
        f = x + P * x**N
        g = y
        det = expand(diff(f, x) * diff(g, y) - diff(f, y) * diff(g, x))
        expected = expand(1 + N * P * x ** (N - 1))
        ok &= check(f"T5 formula N={N} g=y", simplify(det - expected) == 0)
        # det-1 = N P x^{N-1}: vanishes iff P=0 (as poly identity)
        # both components
        f = x + P * x**N
        g = y + Q * x**N
        det = expand(diff(f, x) * diff(g, y) - diff(f, y) * diff(g, x))
        pd = Poly(det - 1, x)
        coef = expand(pd.coeff_monomial(x ** (N - 1)))
        ok &= check(
            f"T5 both N={N} [x^{N-1}]=N*P",
            simplify(coef - N * P) == 0,
        )
        det_P0 = expand(det.subs({c: 0 for c in P_c}))
        expected2 = expand(1 + diff(Q, y) * x**N)
        ok &= check(
            f"T5 both N={N} P=0 => 1+Q'x^N",
            simplify(det_P0 - expected2) == 0,
        )
    # poly2 spot checks
    for N in range(2, min(nmax, 12) + 1):
        for j in range(0, min(dy, 4) + 1):
            f = padd(X, pmul(ppow(X, N), ppow(Y, j)))
            if is_const_nz(jac_det(f, Y)):
                return check(f"T5 poly2 N={N} j={j}", False)
    ok &= check("T5 poly2 spot no Keller", True)
    return ok


