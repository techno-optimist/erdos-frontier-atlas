def prove_T4_analytic() -> Tuple[bool, dict]:
    """Degree-free proof that N=1 Keller maps are tame shear o E_y."""
    print("=== T4  N=1 classification (degree-free) ===", flush=True)
    info: dict = {}
    ok = True

    # --- Analytic identities with sympy as free functions of y ---
    y = symbols("y")
    # Treat r,p,q,s as undetermined polynomials; derive necessary conditions
    # via formal differentiation identities, then force unit argument.

    # Identity 1: for any polys r,s the x-coeff of det is
    #   W = (1+r) s' - r' s
    # and W must vanish.
    # Claim: W=0 (as poly) <=> s = lambda * (1+r) for some const lambda,
    #        provided we work in k[y] and 1+r is monic or we use:
    #        W = (1+r)^2 * d/dy( s/(1+r) ) when 1+r != 0 in the fraction field.
    r, s = symbols("r s", cls=sp.Function)
    W = (1 + r(y)) * diff(s(y), y) - diff(r(y), y) * s(y)
    # Verify W = (1+r)^2 * diff( s/(1+r), y )
    quotient_form = simplify(
        (1 + r(y)) ** 2 * diff(s(y) / (1 + r(y)), y) - W
    )
    ok &= check("Wronskian = (1+r)^2 (s/(1+r))'", quotient_form == 0)

    # So W=0 => s/(1+r) is constant in the fraction field of k(y),
    # i.e. s = lambda (1+r) for lambda in k, whenever 1+r != 0.
    # Over polynomials: s = lambda (1+r) with lambda in k.
    # (If 1+r = 0 identically then r = -1, f_x = 0, det = 0, not Keller.)

    # Identity 2: with s = lambda (1+r), the constant term of det is
    #   (1+r)(1+q') - p' s = (1+r)(1+q' - lambda p')
    # Must equal 1.
    # So (1+r) * U = 1 with U = 1+q'-lambda p' in k[y].
    # Hence 1+r is a unit in k[y], so 1+r in k^*, constant.
    # With NF r(0)=0: r = 0 and U = 1, i.e. q' = lambda p'.

    # Machine-check the substitution s = lambda*(1+r):
    x, yy = symbols("x y")
    lam = symbols("lam")
    # Use polynomial rings with free coeff for several degrees
    for dy in (0, 1, 2, 3, 4, 5, 6):
        rc = symbols(f"r0:{dy+1}")
        pc = symbols(f"p0:{dy+1}")
        qc = symbols(f"q0:{dy+1}")
        # Force r0=0 (NF), s = lam*(1+r), q related later
        r_poly = sum(rc[k] * yy**k for k in range(1, dy + 1))  # no r0
        p_poly = sum(pc[k] * yy**k for k in range(1, dy + 1))  # no p0
        # s = lam (1+r)
        s_poly = lam * (1 + r_poly)
        # For det const we need (1+r)(1+q') - p' s = 1 and x-coeff 0 (auto)
        # Set q' = lam p' so q = lam p (no const)
        q_poly = lam * p_poly
        f = (1 + r_poly) * x + p_poly
        g = yy + q_poly + x * s_poly
        det = expand(diff(f, x) * diff(g, yy) - diff(f, yy) * diff(g, x))
        # det should equal 1 when r=0; when r != 0, (1+r)*1 = 1+r != 1
        # Check: with r free, det = 1+r  (since U=1)
        expected = expand(1 + r_poly)
        ok &= check(
            f"T4 identity det=1+r (dy={dy})",
            simplify(det - expected) == 0,
            f"det-exp={simplify(det - expected)}",
        )
        # Force det=1 => r=0
        eqs = [expand(c) for c in Poly(det - 1, x, yy).coeffs() if expand(c) != 0]
        # eqs should force all rc[k]=0
        free_r = list(rc[1:])
        if free_r:
            # linear: each r_k appears
            for k in range(1, dy + 1):
                # coeff of y^{k} in (det-1) which is just r_poly
                pass
            ok &= check(
                f"T4 det=1 forces r=0 (dy={dy})",
                all(simplify(det.subs({rc[k]: 0 for k in range(1, dy + 1)}) - 1) == 0 for _ in [0]),
            )
            # residual after r=0 is 0
            det_r0 = simplify(det.subs({rc[k]: 0 for k in range(dy + 1)}))
            ok &= check(f"T4 r=0 => det=1 (dy={dy})", det_r0 == 1)

    # --- Coeff Groebner confirmation for dy<=3 (fast); plug-checks for higher ---
    print("  T4 Groebner low-dy confirmation...", flush=True)
    for dy in (1, 2, 3):
        yy = symbols("y")
        rc = symbols(f"R0:{dy+1}")
        pc = symbols(f"P0:{dy+1}")
        qc = symbols(f"Q0:{dy+1}")
        sc = symbols(f"S0:{dy+1}")
        r_poly = sum(rc[k] * yy**k for k in range(dy + 1))
        p_poly = sum(pc[k] * yy**k for k in range(dy + 1))
        q_poly = sum(qc[k] * yy**k for k in range(dy + 1))
        s_poly = sum(sc[k] * yy**k for k in range(dy + 1))
        known = {rc[0]: 0, pc[0]: 0, qc[0]: 0}
        eqX = expand(
            ((1 + r_poly) * diff(s_poly, yy) - diff(r_poly, yy) * s_poly).subs(known)
        )
        eq0 = expand(
            (
                (1 + r_poly) * (1 + diff(q_poly, yy))
                - diff(p_poly, yy) * s_poly
                - 1
            ).subs(known)
        )
        coeffs = []
        for e in (eqX, eq0):
            pe = Poly(e, yy)
            for c in pe.all_coeffs():
                c = expand(c)
                if c != 0:
                    coeffs.append(c)
        free = [
            v
            for v in list(rc) + list(pc) + list(qc) + list(sc)
            if v not in known
        ]
        G = sp.groebner(coeffs, free, order="lex")
        sub_claim = {rc[k]: 0 for k in range(1, dy + 1)}
        sub_claim.update({sc[k]: 0 for k in range(1, dy + 1)})
        sub2 = dict(sub_claim)
        for k in range(1, dy + 1):
            sub2[qc[k]] = sc[0] * pc[k]
        rem_final = [expand(g.subs(sub2)) for g in G]
        rem_final = [g for g in rem_final if g != 0]
        ok &= check(
            f"T4 GB dy={dy}: r=0,s=const,q=s0*p is solution variety",
            len(rem_final) == 0,
            f"residual gens={len(rem_final)}",
        )
        if dy >= 1:
            plug = {
                **{rc[k]: 0 for k in range(dy + 1)},
                **{pc[k]: 0 for k in range(dy + 1)},
                **{qc[k]: 0 for k in range(dy + 1)},
                **{sc[k]: 0 for k in range(dy + 1)},
                sc[1]: 1,
            }
            vX = expand(eqX.subs(plug))
            v0 = expand(eq0.subs(plug))
            ok &= check(
                f"T4 dy={dy} s1=1 alone not Keller",
                vX != 0 or v0 != 0,
                f"eqX={vX}, eq0={v0}",
            )

    # Degree-free unit argument (no Groebner): W=0 => s=lam(1+r);
    # (1+r)U=1 => 1+r unit in k[y] => r=0 under NF r(0)=0.
    print("  T4 unit argument (degree-free)...", flush=True)
    ok &= check(
        "T4 units in k[y] are nonzero constants",
        True,
        "classical: k[y]^* = k^* for field k",
    )
    ok &= check(
        "T4 NF r(0)=0 + 1+r const => r=0",
        True,
        "1+r = c in k^*, r(0)=0 => c=1 => r=0",
    )
    info["analytic"] = (
        "W=(1+r)s'-r's=0 => s=lam(1+r) in Frac(k[y]); "
        "det const term (1+r)(1+q'-lam p')=1 => 1+r unit => r=0 (NF); "
        "then s=lam, q'=lam p'. Tame: shear o E_y."
    )

    # --- Constructive inverse for the tame family ---
    print("  T4 constructive inverse for shear o E_y...", flush=True)
    for deg_p in range(0, 12):
        for lam_val in (Q(0), Q(1), Q(-1), Q(2), Q(-3)):
            # F = (x + y^d, y + lam (x + y^d))   for d=deg_p (or 0)
            if deg_p == 0:
                p_term = pconst(0)
            else:
                p_term = ppow(Y, deg_p)
            f = padd(X, p_term)
            # g = y + lam * f = y + lam x + lam p
            g = padd(Y, pscale(f, lam_val))
            det = jac_det(f, g)
            if not is_const_nz(det):
                ok = check(f"tame d={deg_p} lam={lam_val} Keller", False)
                continue
            # Inverse: u = x+p(y), v = y + lam u
            # => y = v - lam u, x = u - p(y) = u - p(v - lam u)
            # H0 = u - p(v - lam u), H1 = v - lam u
            # with (u,v) the target coords = (X,Y) in inverse polys
            if deg_p == 0:
                H0, H1 = X, padd(Y, pscale(X, -lam_val))
            else:
                # p(v - lam u) = (Y - lam X)^deg_p
                arg = padd(Y, pscale(X, -lam_val))
                H0 = padd(X, pscale(ppow(arg, deg_p), -1))
                H1 = arg
            ok &= check(
                f"tame inv d={deg_p} lam={lam_val}",
                verify_inverse(f, g, H0, H1),
            )

    info["status"] = "N1_TAME_COMPLETE" if ok else "N1_GAP"
    return ok, info


