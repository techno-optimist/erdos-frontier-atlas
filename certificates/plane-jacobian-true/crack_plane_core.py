# ---------------------------------------------------------------------------

def prove_T2(dlead: int = 8) -> bool:
    print("=== T2  pure-power leading force / non-pure blocked ===", flush=True)
    ok = True
    # (a) pure power alone with axis: elementary works
    for d in range(2, dlead + 1):
        f, g = padd(X, ppow(Y, d)), Y
        ok &= check(f"pure axis d={d} Keller", is_const_nz(jac_det(f, g)))

    # (b) non-pure binary forms as sole leading of both components: not Keller
    # R = x^{d-1} y  (two distinct factors) as leading of f, g = y
    for d in range(2, dlead + 1):
        # f = x + x^{d-1} y, g = y  — has cross term, should fail
        mon = pmul(ppow(X, d - 1), Y)
        f = padd(X, mon)
        g = Y
        ok &= check(
            f"non-pure x^{d-1}y in f not Keller",
            not is_const_nz(jac_det(f, g)),
        )
        # R = x^2 y^{d-2} for d>=3
        if d >= 3:
            mon2 = pmul(ppow(X, 2), ppow(Y, d - 2))
            ok &= check(
                f"non-pure x^2 y^{d-2} not Keller",
                not is_const_nz(jac_det(padd(X, mon2), Y)),
            )
        # Both components proportional non-pure: f = x + R, g = y + R
        # R = x^{d-1} y
        R = pmul(ppow(X, d - 1), Y)
        f = padd(X, R)
        g = padd(Y, R)
        ok &= check(
            f"prop non-pure both d={d} not Keller",
            not is_const_nz(jac_det(f, g)),
        )

    # (c) General binary form as leading of f only, g=y: det=1+R_x,
    # so Keller iff R_x=0 iff R is pure in y (no x).
    x, y = symbols("x y")
    for d in range(2, min(dlead, 8) + 1):
        coeffs = symbols(f"c0:{d+1}")
        R = sum(coeffs[k] * x ** (d - k) * y**k for k in range(d + 1))
        f = x + R
        g = y
        det = expand(diff(f, x) * diff(g, y) - diff(f, y) * diff(g, x))
        Rx = expand(diff(R, x))
        ok &= check(f"T2 binary d={d} det=1+R_x", simplify(det - (1 + Rx)) == 0)
        # R_x=0 as poly => coeffs of all x-powers in R vanish
        # R = sum c_k x^{d-k} y^k; R_x = sum c_k (d-k) x^{d-k-1} y^k
        # R_x=0 => c_k=0 whenever d-k >= 1 i.e. k < d. Only c_d free.
        for k in range(d):
            # isolate: set c_k=1, others 0, R_x must be nonzero
            sub = {coeffs[j]: (1 if j == k else 0) for j in range(d + 1)}
            if simplify(Rx.subs(sub)) == 0 and (d - k) >= 1:
                ok = check(f"T2 d={d} c_{k} should affect R_x", False)
                return ok
        # pure y^d: c_d=1, others 0 => R_x=0
        sub_pure = {coeffs[j]: (1 if j == d else 0) for j in range(d + 1)}
        ok &= check(
            f"T2 d={d} pure y^d has R_x=0",
            simplify(Rx.subs(sub_pure)) == 0,
        )
    return ok


