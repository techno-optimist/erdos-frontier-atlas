# ---------------------------------------------------------------------------

def prove_T6(dmax: int = 20) -> bool:
    print("=== T6  exotic single-monom obstruction ===", flush=True)
    ok = True
    n_ex = n_br = 0
    n_el = n_el_ok = 0
    for j in range(0, dmax + 1):
        for i in range(0, dmax + 1 - j):
            if i + j < 2:
                continue
            mon = pscale(pmul(ppow(X, i), ppow(Y, j)), Q(1))
            # E_y
            if i == 0 and j >= 2:
                n_el += 1
                if is_const_nz(jac_det(padd(X, mon), Y)):
                    n_el_ok += 1
                continue
            # E_x
            if j == 0 and i >= 2:
                n_el += 1
                if is_const_nz(jac_det(X, padd(Y, mon))):
                    n_el_ok += 1
                # wrong side: x^i in f
                n_ex += 1
                if not is_const_nz(jac_det(padd(X, mon), Y)):
                    n_br += 1
                continue
            # cross
            if i >= 1 and j >= 1:
                for f, g in (
                    (padd(X, mon), Y),
                    (X, padd(Y, mon)),
                    (padd(X, mon), padd(Y, mon)),
                ):
                    n_ex += 1
                    if not is_const_nz(jac_det(f, g)):
                        n_br += 1
            # pure y in g (exotic)
            if i == 0 and j >= 2:
                n_ex += 1
                if not is_const_nz(jac_det(X, padd(Y, mon))):
                    n_br += 1
    ok &= check("E_x/E_y monoms Keller", n_el == n_el_ok, f"{n_el_ok}/{n_el}")
    ok &= check("exotic monoms break Keller", n_ex == n_br, f"{n_br}/{n_ex}")
    return ok


