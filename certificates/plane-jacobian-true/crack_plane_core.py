def prove_T3(dsample: int = 10) -> bool:
    print("=== T3  GL(2) conjugates of elementary are Keller+invertible ===", flush=True)
    ok = True
    for d in range(2, dsample + 1):
        E0, E1 = padd(X, ppow(Y, d)), Y
        # L = (x+2y, 3x+y), det L = 1-6 = -5
        L0 = padd(X, pscale(Y, Q(2)))
        L1 = padd(pscale(X, Q(3)), Y)
        Linv = invert_affine(Q(0), Q(1), Q(2), Q(0), Q(3), Q(1))
        assert Linv is not None
        e_on = compose(E0, E1, Linv[0], Linv[1])
        F = compose(L0, L1, e_on[0], e_on[1])
        detF = jac_det(F[0], F[1])
        ok &= check(f"conj E_y^{d} Keller", is_const_nz(detF))
        Einv0, Einv1 = padd(X, pscale(ppow(Y, d), -1)), Y
        G = compose(L0, L1, *compose(Einv0, Einv1, Linv[0], Linv[1]))
        ok &= check(f"conj E_y^{d} inverse", verify_inverse(F[0], F[1], G[0], G[1]))

        # Dual E_x conjugate
        Ex0, Ex1 = X, padd(Y, ppow(X, d))
        e_on2 = compose(Ex0, Ex1, Linv[0], Linv[1])
        F2 = compose(L0, L1, e_on2[0], e_on2[1])
        ok &= check(f"conj E_x^{d} Keller", is_const_nz(jac_det(F2[0], F2[1])))
        Exinv0, Exinv1 = X, padd(Y, pscale(ppow(X, d), -1))
        G2 = compose(L0, L1, *compose(Exinv0, Exinv1, Linv[0], Linv[1]))
        ok &= check(
            f"conj E_x^{d} inverse",
            verify_inverse(F2[0], F2[1], G2[0], G2[1]),
        )
    return ok


