# Multi-mixed pair obstruction (two exotic monoms cannot cancel)
# ---------------------------------------------------------------------------

def prove_pair_exotic(dmax: int = 6) -> bool:
    print("=== PAIR  two-exotic cancellation blocked ===", flush=True)
    ok = True
    n_tot = n_br = 0
    monoms = [
        (i, j)
        for tot in range(2, dmax + 1)
        for i in range(tot + 1)
        for j in [tot - i]
    ]
    cross = [(i, j) for i, j in monoms if i >= 1 and j >= 1]
    wrong_f = [(i, 0) for i in range(2, dmax + 1)]
    wrong_g = [(0, j) for j in range(2, dmax + 1)]
    exotic_a = cross + wrong_f
    exotic_b = cross + wrong_g

    for idx, m1 in enumerate(exotic_a):
        for m2 in exotic_a[idx + 1 :]:
            mon1 = pmul(ppow(X, m1[0]), ppow(Y, m1[1]))
            mon2 = pmul(ppow(X, m2[0]), ppow(Y, m2[1]))
            f = padd(X, mon1, mon2)
            g = Y
            n_tot += 1
            if not is_const_nz(jac_det(f, g)):
                n_br += 1
            else:
                return check(f"UNEXPECTED Keller pair f {m1}+{m2}", False)

    # sample product of exotic_a x exotic_b (cap size)
    ea = exotic_a[:12]
    eb = exotic_b[:12]
    for m1 in ea:
        for m2 in eb:
            mon1 = pmul(ppow(X, m1[0]), ppow(Y, m1[1]))
            mon2 = pmul(ppow(X, m2[0]), ppow(Y, m2[1]))
            f = padd(X, mon1)
            g = padd(Y, mon2)
            n_tot += 1
            if not is_const_nz(jac_det(f, g)):
                n_br += 1
            else:
                return check(f"UNEXPECTED Keller mixed pair {m1}|{m2}", False)

    ok &= check("pair exotic all break", n_tot == n_br, f"{n_br}/{n_tot}")
    return ok


