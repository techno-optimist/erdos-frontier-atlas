def homog(p: dict, d: int) -> dict:
    return {m: c for m, c in p.items() if m[0] + m[1] == d}


def pure_power_poly(p: Q, q: Q, d: int, scale: Q = Q(1)) -> dict:
    ell = padd(pscale(X, p), pscale(Y, q))
    return pscale(ppow(ell, d), scale)


def prove_T1(samples: int = 60) -> bool:
    print("=== T1  leading Jac vanishes (top det = det J lead) ===", flush=True)
    ok = True
    n_ok = 0
    n_tot = 0
    for d in range(2, 8):
        for p, q, a, b in product([Q(-2), Q(-1), Q(0), Q(1), Q(2)], repeat=4):
            if p == 0 and q == 0:
                continue
            if a == 0 and b == 0:
                continue
            fd = pure_power_poly(p, q, d, a)
            gd = pure_power_poly(p, q, d, b)
            f = padd(fd, X, pscale(ppow(Y, min(d - 1, 3)), Q(2)))
            g = padd(gd, Y, pscale(ppow(X, min(d - 1, 3)), Q(-1)))
            full = jac_det(f, g)
            top = {m: c for m, c in full.items() if m[0] + m[1] == 2 * d - 2}
            lead = jac_det(homog(f, d), homog(g, d))
            n_tot += 1
            if poly_eq(top, lead):
                n_ok += 1
            if n_tot >= samples:
                break
        if n_tot >= samples:
            break
    ok &= check("top det identity", n_ok == n_tot, f"{n_ok}/{n_tot}")

    # Proportional leading => det J lead = 0
    n_z = n_t = 0
    for d in range(2, 10):
        for p, q in [(Q(1), Q(0)), (Q(0), Q(1)), (Q(1), Q(1)), (Q(2), Q(-1)), (Q(-3), Q(1))]:
            for a, b in [(Q(1), Q(0)), (Q(0), Q(1)), (Q(1), Q(1)), (Q(3), Q(-2))]:
                fd = pure_power_poly(p, q, d, a)
                gd = pure_power_poly(p, q, d, b)
                n_t += 1
                if jac_det(fd, gd) == {}:
                    n_z += 1
    ok &= check("det J(alpha ell^d, beta ell^d)=0", n_z == n_t, f"{n_z}/{n_t}")
    return ok


